function caseOnCommands(commands) {

	if (commands.length == 0) return;

	var command = commands.shift().split(' ');
	switch(command[0]) {
            case "TOURNAMENT_START":
                tournamentStartHandler();

                break;

            case "TOURNAMENT_END":
                tournamentEndHandler();
                break;

            case "HIDDEN_LETTERS":
                hiddenLettersHandler(command[1], command[2]);
                break;

            case "BID":
                bidHandler(command[1], command[2]);
                break;

            case "WORD_SUBMISSION":
                wordSubmissionHandler(command[1], command[2], command[3]);
                break;

            case "ROUND_START":
                roundStartHandler(command[1]);
                break;

            case "ROUND_END":
                roundEndHandler(command[1], command[2], command[3]);
                break;

            case "USER_FINAL_SCORE":
                userFinalScoreHandler(command[1], command[2]);
                break;

            case "GAME_START":
                gameStartHandler(command[1]);
                break;

            case "GAME_END":
                gameEndHandler();
                break;
	}

	setTimeout(function(){caseOnCommands(commands);}, 100);
}

